/// <summary>
///     Enumeration of all available directions for slide animations.
/// <summary>
public enum AnimationSlideDirection
{
    /// <summary>
    ///     Slide from/to the left
    /// </summary>
    Left = 0,

    /// <summary>
    ///     Slide from/to the top
    /// </summary>
    Top = 1,

    /// <summary>
    ///     Slide from/to the right
    /// </summary>
    Right = 2,

    /// <summary>
    ///     Slide from/to the bottom
    /// </summary>
    Bottom = 3
}
